#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 65 && str[a] <= 90)
			a++;
		else
			return (0);
	}
	return (1);
}
