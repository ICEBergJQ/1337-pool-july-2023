#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 65 && str[a] <= 90)
		{
			str[a] += 32;
		}
			a++;
	}
	return (str);
}
