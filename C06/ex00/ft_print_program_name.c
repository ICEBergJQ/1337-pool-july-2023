#include <unistd.h>

void	ft(char *c)
{
	int	i;

	i = 0;
	while (c[i] != '\0')
	{
		write(1, &c[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	(void)argc;
	ft(argv[0]);
	write(1, "\n", 1);
}
