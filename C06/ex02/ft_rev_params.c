#include <unistd.h>

void	ft(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	a;
	int	b;

	b = argc - 1;
	while (b > 0)
	{
		a = 0;
		while (argv[b][a] != 0)
		{
			ft(argv[b][a]);
			a++;
		}
		b--;
		write(1, "\n", 1);
	}
}
