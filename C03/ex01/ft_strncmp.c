#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	a;

	a = 0;
	while (s1[a] != '\0' && s2[a] != '\0' && s1[a] == s2[a] && n != 0)
	{
		a++;
		n--;
	}
	if (n == 0)
	{
		return (0);
	}
	else
		return (s1[a] - s2[a]);
}
