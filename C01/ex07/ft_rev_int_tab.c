#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	sw;

	a = 0;
	b = size - 1;
	while (a < b)
	{
		sw = tab[a];
		tab[a] = tab[b];
		tab[b] = sw;
		a++;
		b--;
	}
}
